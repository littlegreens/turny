  async function addGuestAssignment(date: string, shiftTypeId: string, label: string, color: string) {
    const trimmed = label.trim();
    if (!canEdit || !trimmed) return;

    // Evita di “ricreare” persone già presenti: gli utenti possono solo trascinarle dalla colonna sinistra.
    const normalized = trimmed.replace(/\s+/g, " ").toLowerCase();
    const matchesExistingMember = members.some((m) => (m.label ?? "").trim().replace(/\s+/g, " ").toLowerCase() === normalized);
    if (matchesExistingMember) {
      showToast("error", "Questa persona esiste già: trascinala dalla lista invece di inserirla come extra.");
      return;
    }

    setLoadingKey(`${date}|${shiftTypeId}|guest`);
    const res = await fetch(`/api/schedules/${scheduleId}/assignments`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ date, shiftTypeId, guestLabel: trimmed, guestColor: color }),
    });
    const payload = (await res.json()) as { error?: string };
    setLoadingKey(null);
    if (!res.ok) {
      showToast("error", payload.error ?? "Errore salvataggio");
      return;
    }
    setCellAddOpen(null);
    setGuestNameDraft("");
    showToast("success", "Persona extra aggiunta.");
    router.refresh();
  }

